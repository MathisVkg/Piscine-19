char	*ft_strupcase(char *str)
{
	int nbr;

	nbr = 0;
	while (str[nbr] != '\0')
	{
		if (str[nbr] >= 97 && str[nbr] <= 122)
		{
			str[nbr] = str[nbr] - 32;
		}
		nbr++;
	}
	return (str);
}

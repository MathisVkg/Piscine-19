int	ft_strncmp(char *s1, char *s2, unsigned n)
{
	unsigned int m;

	m = 0;
	while ((s1[m] != '\0' || s2[m] != '\0') && m < n)
	{
		if (s1[m] != s2[m])
			return (s1[m] - s2[m]);
		m++;
	}
	return (0);
}
